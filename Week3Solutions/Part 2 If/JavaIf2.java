public class JavaIf2{
	public static void main (String []args){
		//Declare variable
		double num1;
		num1 = 0.25484;

		if(num1 < 0)
		{
			System.out.println("Number is less than zero");
		}

		if(num1 > 0)
		{
			System.out.println("Number is greater than zero");
		}

		if(num1 == 0)
		{
			System.out.println("Number is zero");
		}
	}
}


